-- ============================================================
--  Защита от подбора пароля (brute-force) для админки и кабинета врача
--
--  Что делает: создаёт таблицу, где серверные функции считают неудачные
--  попытки входа и временно блокируют дальнейшие попытки. Это главная
--  реальная защита от хакеров, которые перебирают пароли программой.
--
--  КАК ЗАПУСТИТЬ (один раз):
--  Supabase → SQL Editor → New query → вставь весь текст → Run.
--  Повторный запуск безопасен.
-- ============================================================

-- Таблица «счётчик попыток входа».
--   key            — кого считаем, например 'admin:1.2.3.4' или 'doctor:5:1.2.3.4'
--   fails          — сколько подряд неудачных попыток
--   blocked_until  — до какого момента вход заблокирован (NULL = не заблокирован)
create table if not exists public.auth_throttle (
  key           text primary key,
  fails         int not null default 0,
  blocked_until timestamptz,
  updated_at    timestamptz default now()
);

-- Защита таблицы: включаем RLS и НЕ создаём политик.
--   Это значит, что публичный (анонимный) ключ сайта НЕ может ни читать,
--   ни менять эти данные. Доступ есть только у серверных функций
--   (они работают с service_role и обходят RLS).
alter table public.auth_throttle enable row level security;

